/// </summary>
public enum CleaningAction
{
    /// <summary>
    /// Start cleaning.
    /// </summary>
    Start,

    /// <summary>
    /// Resume cleaning.
    /// </summary>
    Resume,

    /// <summary>
    /// Pause cleaning.
    /// </summary>
    Pause,

    /// <summary>
    /// Stop cleaning.
    /// </summary>
    Stop
}
